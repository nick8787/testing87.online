								<br>
								<u>Это архитектура проекта. Архитектура его строения.</u>
<br>
<b>Монолит</b> – сборка всех функций и элементов слитно. А <b>микросервис</b> – разбиваются на архитектуру, каждая функция и элемент будет архитектурой.
<br>
Разница между ними:
<br>
<b>Монолит</b> дешевле <b>микросервиса</b>
<br>
<b>Монолит</b> быстрее <b>микросервиса</b>
<br>
Если возникает ошибка: в <b>монолите</b> перестает работать все, а в <b>микрсервисе</b> – падает только одна архитектура, то есть одна функция.
<br>
Чаще всего используется <b>микросервисы.</b>



								<p>
								<b><u>1.Груминг беклога (grooming backlog)</b></u>
								<br>
Мероприятие по грумингу представляет собой оптимизацию и обновления беклога, возможно доработка беклога. Также разбираются пользовательские истории, и есть ли понимание у каждого участника команды этих User Story.
User story – ее задача максимально конкретно описать особенности конкретного продукта, его функциональные возможности и ценность для конечного потребителя.
<br>
<b><u>2.Планирования спринта (sprint planning meeting)</b></u>
<br>
Планирование спринта заключается в наборе команды, которая будет выполнять задачи определенный отрезок времени. Во время митинга команда рассматривает задачи и выставляет приоритетность. Митинг заканчивается, когда будет поставлена четкая цель и каждый будет понимать, что он будет делать и как. 
<br>
<b><u>3.Ежедневный митинг (daily meeting)</b></u>
<br>
Скрам-встречи (или стендапы) – тут команда анализирует цели, сверяется с графиком, ищет явные и перспективные проблемы. Стендапы проходят ежедневно в одно и то же время. Каждому участнику заранее дают вопросы, чтоб он подготовил ответ. Благодаря митингам команда знает свою успеваемость и знает, когда сможет закончить все задачи. 
<br>
<b><u>4.Ретроспектива</b></u>
<br>
Обсуждение после окончания спринта, Анализ всех событий. Что было хорошо, а что можно улучшить в след спринте. Длится около часа митинг.
<br>
<b><u>5.Осмотр спринта (sprint review)</b></u>
<br>
Углубление команды в готовый продукт. Демонстрация возможностей перед менеджментом и клиентом.
<br>
<b><u>6.Demo</b></u>
<br>
Это митинг, который является результатом спринта, где каждый презентует свою работу.


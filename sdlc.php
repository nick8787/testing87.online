							<div class='container-tx2-block darkmode-txt'>
								<p>
								<b>Agile</b> – концепция того, как должна хорошо выглядеть методология.
								<br>
Своими словами: это набор гибких методов и принципов в работе над проектом для эффективности выполнения задач и улучшения проекта. 
<br>
<b><u>Scrum Kanban</b></u>
<br>
<b>Kanban</b> – это конвейер, сколько задач дадут, столько и надо выполнит, нет определённого количества задач. В канбане есть доска задач, если кто-то где-то запнулся, вся команда должна помочь решить ее.
<br>
<b>Scrum</b> – более популярная методология, там есть четкость по спринтам, четкость по митингам (туда входит: груминг, пленинг, stand up'ы, ретроспектива, sprint review и demo). В скраме есть <u>беклог</u> – это четкий список задач, после планирования, задачи берутся в спринты для их обработки.<br> Члены скрама: Product Owner, Scrum master and Scrum team.
<br>
<b>Спринты (Sprint)</b> – часовой промежуток, где команда набирает задачи для дальнейшего выполнения. В основном длится 2-4 недели.
<br>
<b><u>Waterfall</b></u>
<br>
Это модель разработки ПО, в основном это проекты с абсолютной точностью, например это про медицину, про машину, про самолеты, должна быть четкая точность выполнения задач, в скраме, например, допустимо наличие бага, его потом можно пофиксить, а вотерфоле – не допустимо. Но даже если и придется внести изменения, необходимо будет начать все сначала.


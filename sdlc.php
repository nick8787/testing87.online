							<div class='container-tx2-block darkmode-txt'>
								<p>
								<b><u>Continuous integration / Continuous Delivery</b></u>
								<br>
Это беспрерывная автоматизированная разработка ПО, которая обеспечивает скорость разработки и поэтапную автоматизацию перехода задачи с одной фазы в другую.
<br>
<b>Ci</b> – дорелизная версия продукта, где пишется код, и происходит сборка, где можно еще пофиксить баги. Тут используется, к примеру, Jenkins, GitLab, GitHub Actions, Ajur DevOps для создания и управления Ci.
<br>
<b>СD</b> – это уже некий релиз, то есть развертывание продукта. Это возможность собрать всю необходимую информацию, чтоб потом продукт стал лучше.
К примеру, ОС iOS – была создана 1 версия, сначала она прошла Ci, потом CD, то есть была выпущена для пользования и благодаря CD, то есть уже какой-то готовой версии продукта, можно что-то добавлять или изменять и это все происходит в Ci. Получается происходит постоянная автоматизированная интеграция ПО.



								<h2>Иерархия коллекций в JAVA</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
									 Иерархия коллекций состоит из <b>интерфейсов</b> и <b>классов</b>. Это способ хранения и обработки данных. Это что-то вроде массивов, только больше места для значений. С ее помощью классы коллекций реализуют различные <b>алгоритмы</b> и <b>структуры данных</b>, например, стек, очередь, дерево и т.д. В JAVA множество коллекций, у каждого есть свой интерфейс, который определяет свой функционал. Среди интерфейсов есть следующие:
							 <br>
							 Существуют две коллекции <b><u>COLLECTION interface</b></u> and <b><u>MAP interface.</b></u>
							 <br><br>
<b><u>COLLECTION</b></u> наследует интерфейсы: <b>LIST, QUEUE, SET.</b>
<br>
<b>LIST (обьекты в виде списка)</b> реализуется в классах:
<br>
<u>ArrayList</u> – список массивов, элементы пронумерованы(уникальный номер). Стандартная длина ячеек - 10.
<br>
<u>LinkedList</u> – это двусвязный список, к примеру, 2ой элемент посылается на 1 и на 3, все элементы взаимодействуют между собой.
<br>
<b>SET (все обьекты уникальны, не повторяются)</b> реализуется в классах:
<br>
<u>HashSet</u> (каждый объект идентифицируется по хешу, добавляются в хаотичном порядке)
<br>
<u>LinkedHashSet</u> (элементы добавляюстя в том порядке, в котором их добавляют, взаимодействуют между собой)
<br>
<u>TreeSet</u> (элементы соритируются в алфавитном порядке, либо от 1 и по порядку, автоматом удаляюстя уникальные элементы)
<br>
<b>QUEUE (данные в виде очереди: первый зашел, первый вышел)</b> наследует:
<br>
<u>Deque</u> (данные в виде очереди: <b>первый зашел и последний вышел)</b>
<br><br>
<b><u>MAP INTERFACE (набор элементов, которые сортируются по ключ-значению)</b></u> реализуется в классах:
<br>
<u>HashMap</u> (этот класс использует хеш-таблицу для хранения данных)
<br>
<u>LinkedHashMap</u> (сохраняет порядок вставки элементов)
<br>
<u>TreeMap</u> (элементы идут по алфавитному порядку, либо от 1 и по порядку)
<br><u>Ключи одинаковыми быть не могут, все элементы лежат в массиве.</u>

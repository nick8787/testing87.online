								<h2>Обьектно-ориентированное программирование</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
								<b><u>ООП</b></u> – это парадигма (методология) программирования с использованием объектов и классов. <b><u>В ООП</b></u> все объекты взаимодействуют между собой. 
								
								<br><b><u>JAVA</b></u> - это ООПшный язык программирования, базируется на своих принципах и хороша тем, что если код был написан 20 лет назад, то сейчас он скорее всего запуститься.

<br>
Мы привыкли к объектам, они нас окружают и взаимодействуют, например, чтоб доехать куда-то, нужна машина, чтоб покушать – нужна посуда. То есть, мы привыкли мыслить объектно, это и послужило сделать стиль в Java объектно-ориентированным.
<br>
<b><u>Как он работает в Java?</b></u>
<br>
<b>К примеру</b>, я хочу создать телефон. Только я знаю, как он будет работать и какие детали нужны будут и из чего он состоит. Но я один создавать не смогу их, мне нужен штат работников. И чтоб каждый раз не объяснять им как это делается, то необходимо создать чертеж, план и описание. В ООП такой чертёж, план и описание называется <b>классом</b>, а то, что получится в конце, будет – <b>объектом</b>. 
<br>
<b>Класс</b> — это описание еще не созданного объекта, как бы общий шаблон, состоящий из полей, методов и конструктора, а объект – экземпляр класса, созданный на основе этого описания.



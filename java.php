						<h2>DRY KISS SOLID</h2>
					</div>
					<div class='container-tx2-block darkmode-txt'>
						<p>
				<u>Это принципы для упрощения написания кода.</u>
				<br>
<b>DRY – Don't repeat yourself</b> – принцип призывает Вас не повторяться при написании кода. Все что Вы пишите в проекте, должно быть определено только один раз. 
<br>
<b>KISS – keep it simple stupid</b> – делайте вещи проще. Порой наиболее правильное решение – это наиболее простая реализация задачи, в которой нет ничего лишнего.
<br>
<b>SOLID</b> — это принципы разработки программного обеспечения, следуя которым Вы получите хороший код, который в дальнейшем будет хорошо масштабироваться и поддерживаться в рабочем состоянии.
<br>
<b>S - Single Responsibility Principle</b> - Каждый класс должен иметь только одну зону ответственности.
<br>
<b>O - Open closed Principle</b> - Классы должны быть открыты для расширения, но закрыты для изменения.
<br>
<b>L - Liskov substitution Principle</b> - Дочерний класс не должен нарушать логику родительского класса, при этом работа программы не должна измениться.
<br>
<b>I - Interface Segregation Principle</b> - Каждый интерфейс должен быть наделенным смыслом.
<br>
<b>D - Dependency Inversion Principle</b> - Модули верхнего уровня не должны зависеть от модулей нижнего уровня. И те, и другие должны зависеть от абстракции. Абстракции не должны зависеть от деталей. Детали должны зависеть от абстракций.


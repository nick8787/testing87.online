					<div class='container-tx2-block darkmode-txt'>
						<p>
						    <b><u>Циклы</b></u> – это специальная конструкция, за счет которой наше действие может выполнится несколько раз.
						    <br>
В Java есть такие циклы, как: <b>for, while, do-while, forEach, break-continue</b>

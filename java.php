								<h2>4 принципа в ООП</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
								<b><u>1.АБСТРАКЦИЯ </b></u>
								<br>
С помощью абстракции мы выделяем общие важные характеристики объектов. 
<br>
В джаве создается <u>абстрактный класс</u> или <u>интерфейс</u>, чтобы сделать 
шаблон для сохранения метода, который потом реализуется в других классах. <br><b>Разница абстрактного класса и интерфейса</b>: <br>• Абстрактный класс может наследовать один другой класс и наследовать несколько интерфейсов, в то время  как, интерфейс может наследовать только интерфейсы. <br>• Абстрактные классы могут иметь <b>конструкторы</b>, интерфейсы - нет.<br>• Абстрактные классы могут быть public, protected или private, в то время как, интерфейсы - автоматически становятся public, protected или private. <br>• Также в <u>интерфейсах</u> можно реализовать методы. Абстрактный класс - <u>extends</u>, интерфейсы - <u>implements</u>.
<br>
<b><u>2.ИНКАПСУЛЯЦИЯ (Секьюрность методов или классов)</b></u>
<br>
С помощью инкапсуляции мы оставляем наши методы или классы открытыми, закрытыми или защищенными или дефолтными. (private, default, protected, public).
<br>
<b>Private</b> - метод или переменная видна только в гаринцах этого класса. <b>Default</b> - если не прописывается модификатор в методе, значит он будет дефолтным. <b>Protected</b> - используется в классах-наследниках и в packages. <b>Public</b> - повсюду используется.
<br>
<b><u>3.НАСЛЕДОВАНИЕ (функция extends)</b></u>
<br>
В ООП Наследование заключается в использовании уже существующих классов, для создания новых. <u>Дочерний класс</u> создается для того, чтобы использовать его методы или иметь доступ к обьектам родительского класса.
<br>
Например, есть телефон. Телефон может быть сотовым. Сотовый телефон может быть сенсорным или кнопочным. Это наследование от телефона.
<br>
<b><u>4.ПОЛИМОРФИЗМ</b></u>
<br>
Это проявление одного поведения разными способами.
<br>
Существует два вида полиморфизма: <b>1.</b>СТАТИЧЕСКИЙ ПОЛИМОРФИЗМ <b><u>OVERLOAD</b></u>, <u>выполняется во время компиляции</u> - это создание одинаковых методов, но с разными параметрами, <b>2.</b>ДИНАМИЧЕСКИЙ ПОЛИМОРФИЗМ <b><u>OVERRIDE</b></u>, <u>выполняется во время исполнения кода</u> - может переопределять методы в другие классы. Два способа реализации.<br> <u>Assert.assertTrue (тут любые параметры)</u> - это <b>статический полиморфизм</b>, потому что асертов может быть много, но с разными параметрами, а <b>динамический полиморфизм</b> - в тесте, к примеру, создали метод для умножения чисел и в других классах можем его использовать и задавать разные значения.
<br><a href="/img/graphics/about/abstractTest.png" target="_blank">

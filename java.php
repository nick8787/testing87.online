						<p>
					Это интерфейсы, где содержится только 1 метод. Часто используется для созданий лямбда-выражений и анонимных классов, что делает код более компактным. 
					<br>
<u>Есть такие функциональные интерфейсы, как:</u>
<br>
<b>1.	java.util.function.<u>Consumer</u></b> - это функция, которая принимает один аргумент и выполняет какие-то действия над ним, но не возвращает результат.
<br>
Consumer<String> consumer = str -> System.out.println(str); consumer.accept("Привет, мир!"); 
<br>
<b>2.	java.util.function.<u>Supplier</u></b> - это функция, которая не принимает аргументы, но возвращает значение.
<br>
Supplier<Integer> supplier = () -> 42; int result = supplier.get(); 
<br>
<b>3.	java.util.function.<u>Function</u></b> - это функция, которая принимает один аргумент и возвращает результат.
<br>
Function<Integer, String> converter = i -> "Значение: " + i; String result = converter.apply(10); 
<br>
<b>4.	java.util.function.<u>Predicate</u></b> - это функция, которая принимает один аргумент и возвращает булево значение.
<br>
Predicate<Integer> isPositive = i -> i > 0; boolean result = isPositive.test(5); 

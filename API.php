								<h2>API, REST API, типы API</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
									<b><u>API (Application Programming Interface)</u></b> — это взаимодействие между модулями, способ передачи данных между несколькими приложениями. Появился в 2000 году. 
									<br>
<b><u>REST API</u></b> – архитектурный стиль для написания кода.
<br>
В основном используется СВАГЕР для этого, либо POSTMAN – это документация того, как будет работать API.
<br>
<b><u>Типы API</u></b>
<br>
<b>RESTful API:</b>
<br>
Это способ создания веб-сервисов и способ взаимодействия клиента и сервера. Тоже имеет архитектурный стиль.
<br>
Этот тип API использует стандартные HTTP методы (GET, POST, PUT, DELETE) для взаимодействия с удаленными ресурсами, обычно представленными в формате JSON или XML.
<br>
<b>SOAP API:</b>
<br>
Это протокол, который использует XML для обмена сообщениями между компонентами. 
<br>
<b>GraphQL API:</b>
<br>
Это язык запросов для клиентов, чтоб появлялись данные только те, которые им нужны.


								<h2>HTTP-requests:</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
									 <b><u>1.GET запрос</u></b> – используется для просмотра информации с сервера без его изменения.
									 <br>
<b><u>2.POST запрос</u></b> – используется для отправки новых изменений на сервер

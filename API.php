								<h2>Status-codes</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
<b><u>1хх коды</u></b> - информационные
<br>
<b><u>2хх коды</u></b> – Положительный результат. Например, <b>200</b> – ОК(запрос успешно обработан и показаны все данные запроса), <b>201</b> – ОК+CREATED, <b>204</b> – (OK, но ответ не содержит доп данных) Например, я что-то удалил и ничего не вернулось.
<br>
<b><u>3хх коды</u></b> – коды, указывают на перенаправление. Например, <b>301</b> – Перенаправлен на новый URL, <b>302</b> – данные URL временно перемещен, <b>304</b> – Ресурс неизменен.
<br>
<b><u>4хх коды</u></b> – указывают на ошибку со стороны клиента. Например, <b>400</b> – синтаксическая ошибка, <b>401</b> – клиент должен авторизироваться, <b>402</b> - нет оплаты, <b>403</b> – клиент авторизирован, но не имеет доступа, <b>404</b> – ресурс не найден на сервере.
<br>
<b><u>5хх коды</u></b> – указывает на ошибку со стороны сервера. Например, <b>504</b> – превышено время ожидания. 


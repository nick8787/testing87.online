								<h2>Rest-Assured</h2>
							</div>
							<div class='container-tx2-block darkmode-txt'>
								<p>
									 <b><u>REST-ASSURED</u></b> - это библиотека, с помощью которой мы можем автоматизировать Rest API. Используется с JUnit. 
									 <br><u>4 основных Rest-Assured метода:</u>
									 <br><b>Given</b> — позволяет узнать, что было передано в запросе.
<br>
<b>When</b> — с каким методом и на какой эндпойнт отправляем запрос.
<br>
<b>Then</b> — как проверяется пришедший ответ.
<br>
<b>Body</b> — тело, в нашем случае, ответа.
<br><a href="https://fv5-2.failiem.lv/thumb_show.php?i=wv3prvx7xc&view&v=1" target="_blank">
								<span><b>Пример: Тест в Java с исп Rest-Assured + Builder.</b> </span></a>
<br>Также стоит использовать <b>паттерн Builder</b>. С его помощью мы можем задать параметры в запрос, тем самым сделав код читабельней.
<br><br><b><u>Также методы Rest-Assured:</u></b>
<br>
Методы класса <b>Matchers</b> - используются для <u>проверки assert</u> в API ответах, также с помощью отдельного класса <u>Response</u>, с его помощью мы можем проверять статус код, время ожидания, хедеры также. 
<br><b>Query params</b> - метод для передачи параметров.
<br><b>Content</b> - метод для передачи запроса.
<br><b>Header, headers</b> - методы для работы с заголовками (header - для установки 1 заголовка, headers - для нескольких).
<br><b>Extract</b> - метод, чтоб извлечь параметры с хедера.
